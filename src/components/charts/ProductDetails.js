import { PureComponent } from 'react';
import { Cell, Pie, PieChart, Tooltip } from 'recharts';
import data from './json/productDetails.json';

const COLORS = ['#2579f6', '#4de670', '#faa142', '#FF8042', '#e93ff5'];

const RADIAN = Math.PI / 180;
const renderCustomizedLabel = ({
  cx,
  cy,
  midAngle,
  innerRadius,
  outerRadius,
  percent,
  index
}) => {
  const radius = innerRadius + (outerRadius - innerRadius) * 0.5;
  const x = cx + radius * Math.cos(-midAngle * RADIAN);
  const y = cy + radius * Math.sin(-midAngle * RADIAN);

  return (
    <text
      x={x}
      y={y}
      fill="white"
      textAnchor={x > cx ? 'start' : 'end'}
      dominantBaseline="central"
    >
      {`${(percent * 100).toFixed(0)}%`}
    </text>
  );
};

export default class ProductDetails extends PureComponent {
  render() {
    return (
      <>
        <PieChart width={400} height={400}>
          <Pie
            data={data}
            cx={200}
            cy={200}
            labelLine={false}
            label={renderCustomizedLabel}
            outerRadius={80}
            fill="#8884d8"
            dataKey="value"
          >
            {data.map((entry, index) => (
              <Cell
                key={`cell-${index}`}
                fill={COLORS[index % COLORS.length]}
              />
            ))}
          </Pie>
          <Tooltip itemStyle={{
            color: 'whitesmoke'
          }} />
        </PieChart>
        <ul>
          {data.map((entry, index) => (
            <li key={`cell-${index}`} backgroundColor={COLORS[index % COLORS.length]}>
              {entry.name}
            </li>
          ))}
        </ul>
      </>
    );
  }
}
